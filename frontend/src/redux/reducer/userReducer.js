export const sendEmailForResetPasswordReducer = (state = {}, action) => {
  switch (action.type) {
    case EMAIL_RESETPASSWORD_REQUEST:
      return { loading: true };
    case EMAIL_RESETPASSWORD_SUCCESS:
      toast.success(`${action.payload.message}`, {
        position: "bottom-center",
        autoClose: 2000,
        hideProgressBar: true,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: false,
        progress: undefined,
      });
      return { loading: false, resetPassword: action.payload };
    case EMAIL_RESETPASSWORD_FAIL:
      toast.error(`${action.payload}`, {
        position: "bottom-center",
        autoClose: 2000,
        hideProgressBar: true,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: false,
        progress: undefined,
      });
      return { loading: false, error: action.payload };
    default:
      return state;
  }
};

